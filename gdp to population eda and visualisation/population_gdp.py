import pandas as pd 
import matplotlib.pyplot as plt
df = pd.read_csv(r'C:\Users\GOURAV SHARMA\OneDrive\Documents\real_projects\car_sales_data.csv')
print(df.describe())
print(df.duplicated())
print(df.isnull())
