plt.pie(cars_sold, labels = cars_sold.index , autopct = '%.1f%%' , startangle= 90 )
plt.show()
car_prices = df['Price'].value_counts()
plt.figure(figsize = (7,8))
plt.hist(df['Price'], bins= 30 ,color = 'purple',edgecolor = 'black')
